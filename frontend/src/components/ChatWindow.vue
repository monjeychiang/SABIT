  // ËÆæÁΩÆËøûÁª≠Ê∂àÊÅØÁöÑÊúÄÂ§ßÊó∂Èó¥Èó¥Èöî(ÊØ´Áßí)
  const MAX_TIME_DIFF = 5 * 60 * 1000; // 5ÂàÜÈíü
  
  messages.forEach((message, index) => {
    // ÂÖãÈöÜÊ∂àÊÅØÂØπË±°ÔºåÈÅøÂÖç‰øÆÊîπÂéüÂßãÊï∞ÊçÆ
    const clonedMessage = { ...message };
    
    // Âà§Êñ≠ÊòØÂê¶‰∏∫ËøûÁª≠Ê∂àÊÅØ
    if (index > 0) {
      const prevMessage = messages[index - 1];
      const isSameUser = prevMessage.userId === message.userId && prevMessage.type === message.type;
      let prevTime = new Date(prevMessage.time).getTime();
      let currTime = new Date(message.time).getTime();
      
      // Â¶ÇÊûúÊó∂Èó¥Ëß£ÊûêÂ§±Ë¥•Ôºå‰ΩøÁî®ÂΩìÂâçÊó∂Èó¥
      if (isNaN(prevTime) || isNaN(currTime)) {
